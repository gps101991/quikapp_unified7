# Validate generated Dart file
validate_dart_file() {
    log_info "üîç Validating generated Dart file..."
    
    if [[ ! -f "$DART_ENV_FILE" ]]; then
        log_error "‚ùå Generated Dart file not found: $DART_ENV_FILE"
        return 1
    fi
    
    # Check if file contains expected content
    if grep -q "class Environment" "$DART_ENV_FILE"; then
        log_success "‚úÖ Dart file contains Environment class"
    else
        log_error "‚ùå Dart file missing Environment class"
        return 1
    fi
    
    # Check if file contains workflow ID
    if grep -q "workflowId.*${WORKFLOW_ID:-ios-workflow}" "$DART_ENV_FILE"; then
        log_success "‚úÖ Dart file contains correct workflow ID"
    else
        log_warning "‚ö†Ô∏è Dart file may not contain correct workflow ID"
    fi
    
    # Validate Dart syntax
    if command -v dart &> /dev/null; then
        if dart analyze "$DART_ENV_FILE" >/dev/null 2>&1; then
            log_success "‚úÖ Dart file syntax is valid"
        else
            log_warning "‚ö†Ô∏è Dart file may have syntax issues"
            dart analyze "$DART_ENV_FILE" 2>&1 | head -10
        fi
    else
        log_warning "‚ö†Ô∏è Dart not available for syntax validation"
    fi
    
    return 0
}

# Main function
main() {
    log_info "üöÄ Starting Dart environment configuration generation for iOS workflow..."
    
    # Validate environment variables
    validate_required_vars
    
    # Generate Dart environment configuration
    generate_dart_environment
    
    # Validate generated file
    if validate_dart_file; then
        log_success "üéâ Dart environment configuration generated successfully!"
        log_info "üì± Your iOS app can now access all workflow variables through the Environment class"
        
        # Show summary
        log_info "üìä Environment Configuration Summary:"
        log_info "   - App Name: ${APP_NAME:-Not set}"
        log_info "   - Bundle ID: ${BUNDLE_ID:-Not set}"
        log_info "   - Version: ${VERSION_NAME:-Not set} (${VERSION_CODE:-Not set})"
        log_info "   - Workflow: ${WORKFLOW_ID:-ios-workflow}"
        log_info "   - Push Notifications: ${PUSH_NOTIFY:-false}"
        log_info "   - Firebase iOS: ${FIREBASE_CONFIG_IOS:+Enabled}"
        log_info "   - Generated File: $DART_ENV_FILE"
        
    else
        log_error "‚ùå Failed to generate valid Dart environment configuration"
        exit 1
    fi
}

