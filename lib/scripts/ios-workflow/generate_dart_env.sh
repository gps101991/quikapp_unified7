# Validate required environment variables
validate_required_vars() {
    log_info "üîç Validating required environment variables..."
    
    local required_vars=(
        "WORKFLOW_ID"
        "PROJECT_ID"
        "APP_NAME"
        "VERSION_NAME"
        "VERSION_CODE"
        "BUNDLE_ID"
        "APPLE_TEAM_ID"
    )
    
    local missing_vars=()
    
    for var in "${required_vars[@]}"; do
        if [[ -z "${!var:-}" ]]; then
            missing_vars+=("$var")
        fi
    done
    
    if [[ ${#missing_vars[@]} -gt 0 ]]; then
        log_warning "‚ö†Ô∏è Missing required variables: ${missing_vars[*]}"
        log_warning "üîÑ Using default values for missing variables"
        return 1
    else
        log_success "‚úÖ All required variables present"
        return 0
    fi
}

